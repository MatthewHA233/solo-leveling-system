export const ANCHOR_TAXONOMY = `锚点按主人说话的「姿态」分三类（不按名词类型）：
- motive（刺激·动机）：被内容击中、冒出"我想去做某事"的冲动
- view（观点·看法）：下了判断、表达了立场或认识
- practice（教程·实践）：沉淀出可操作的方法、步骤、技巧经验

提取规则：
1. 先判断这番话命中哪几类（通常只有一类，最多两类），再为每个命中的类写一条「锚点句」
2. 锚点句是一句完整的话（10~30 字），单独拿出来也能读懂主人的冲动/判断/做法；不是压缩关键词，更不是光秃名词
3. 锚点句**不带触发条件/来源**（"看完这个视频后""读完那篇文章后"这类前缀一律去掉）。
   锚点要跨语境共享——同一个动机可能被很多个语境先后促成，触发它的语境已由绑定单独记录；
   锚点句只写姿态本身：想做什么 / 怎么判断 / 怎么做。
4. 示例：
   - "看完这视频我都想检查下自己仓库有没有泄露 API Key"
     → [{"keyword":"想检查自己的 GitHub 公开仓库有没有泄露 API Key","category":"motive"}]
     （整句是一股冲动 = 一条 motive；绝不拆成 API Key、GitHub 这种名词碎片；"看完这视频"是触发语境，不进锚点句）
   - 反例：✗"看完视频后想学习正确坐姿" → ✓"想学习正确坐姿与体态影响的知识"
   - "平台不该把安全责任全推给用户"
     → [{"keyword":"平台不该把安全责任全推给用户","category":"view"}]
   - "macOS 终端里截图后要用 Control+V 粘贴，不是 Command+V"
     → [{"keyword":"macOS 终端粘贴截图用 Control+V 而非 Command+V","category":"practice"}]`
